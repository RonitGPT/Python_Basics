#==========================================================================================================
                        # BRANCHING STATEMENTS :- BREAK / CONTINUE
#==========================================================================================================

# BRANCHING 

# BREAK 

for i in range (1,11):
    if (i==6):
        break
    print(i)
#--------------------------------------------------------------------------------------------------

# CONTINUE

for i in range (1,11):
    if (i==6):
        continue
    print(i)
